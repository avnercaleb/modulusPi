/**
 * Function to calculate the checksum of buffer of x bytes
 */
uint8_t RF_CksCalc(RFMSG_STRUCT *msg)
{
    uint8_t i;
    uint8_t *buffPt;
    uint16_t cks=0;

    buffPt = msg;

    for (i=0; i<msg->len-2; i++)
    {
        cks += *(buffPt+i);
    }
    cks += msg->ttl;

    cks = (uint8_t)(cks & 0x00FF);

    return cks;
}



